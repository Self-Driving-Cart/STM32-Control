uint16_t hc_sr04_read (void){
	counter_time = 0;
	HAL_GPIO_WritePin(GPIOD, GPIO_PIN_0,0); 
	delay_us(2);
	HAL_GPIO_WritePin(GPIOD, GPIO_PIN_0,1);
	delay_us(10);
	HAL_GPIO_WritePin(GPIOD, GPIO_PIN_0,0);
	
	while(!HAL_GPIO_ReadPin(GPIOF, GPIO_PIN_0)); 
	while(HAL_GPIO_ReadPin(GPIOF, GPIO_PIN_0)){
	counter_time++;
		delay_us(1);
	}
	return counter_time;
}

